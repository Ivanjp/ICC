public class Problema2{

public static void main(String [] args){
	
	int valor1 = (-1*-1)+1;
	
	int valor2 = (0*0)+1;
	
	int valor3 = (1*1)+1;
	
	int valor4 = (2*2)+1;
	
	System.out.println("Los resultados para la funcion x^2+1 son: " );
	System.out.println("f(-1):"+valor1);
	System.out.println("f(0):"+valor2);
	System.out.println("f(1):"+valor3);
	System.out.println("f(2):"+valor4);
}
}

